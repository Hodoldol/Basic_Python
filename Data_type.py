#자료형을 확인하는 방법 : type()
print(type(100))
print(type("문자열"))
print(type(1.5))

#문자열
print("안녕하세요")
print('안녕하세요')
print("'안녕하세요' 반갑습니다.")

#문자열에서 줄 바꾸는 방법
print("""안녕하세요.
문자열에서 줄을 바꿉니다.
""")

#문자열 반복 연산자 : *
print("반복 " * 3)

#문자열 인덱싱
print("안녕하세요"[1:4])
print("안녕하세요"[-3:-1])
print("안녕하세요"[1:])

#문자열 길이 구하기 : len()
print(len("안녕하세요"))
